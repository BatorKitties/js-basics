
//constants practice
let interestRate = 0.3;

interestRate = 1;  //this will reassign the value of interestRate to 1 which will show up in the console

console.log(interestRate);

//let is used to reassign a value

//const is used to assign a value that will not change

//if you dont need to reassign a value use const

//if you need to reassign a value use let

//more constants practice
const thisRate = 0.3;

thisRate = 1; //this will not work because thisRate is a constant

console.log(thisRate);

//this will show an error in the console because you cannot reassign a constant

//if you try to reassign a constant you will get an error in the console

//constants are used to prevent bugs in your code
